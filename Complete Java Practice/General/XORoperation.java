public class XORoperation {
    public static void main(String[] args) {
        int num1 = 1;
        int num2 = 2;
        int num3 = 4;
        int num4 = 5;
        int num5 = 6;

        int xorOfAll  = num1 ^ num2 ^ num3 ^ num4 ^ num5;
        System.out.println("XOR = :" + xorOfAll);
    }
}
